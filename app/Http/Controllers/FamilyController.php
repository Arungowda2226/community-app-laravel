    /**
     * Store a newly created resource in storage.
     *
     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
    public function store(Request $request)
    {
        $request->validate([
            'inputs.*.name' => 'required'
        ], [
            'inputs.*.name' => 'The Name field is required'
        ]);

        $user_id = Auth::id();
        // $user_id = $logged_in_user->id; // Retrieve the user ID from $logged_in_user variable

        foreach ($request->inputs as $key => $value) {
            $value['user_id'] = $user_id; // Set the user_id value

            Family::create($value);
        }

        return back()->with('success', 'The post has been added!');
    }

